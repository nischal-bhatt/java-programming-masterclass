public class CharAndBoolean {
    public static void main(String[] args) {

        char a = 'D';
        System.out.println(a);

        boolean isCustOver18 = true;

        if (isCustOver18)
        {
            System.out.println("over 18");
        }else
        {
            System.out.println("under 18");
        }
    }
}
